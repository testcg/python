"""
    字面值：各种写法
        数据类型
            int   float   str
            10     1.2    "随便"
"""
# 1. int 字面值
# 十进制DEC：每位用十种状态计数，逢十进一，写法是0~9。
number01 = 10
# 二进制BIN：每位用二种状态计数，逢二进一，写法是0~1。
number02 = 0b10
# 八进制OCT：每位用八种状态计数，逢八进一，写法是0~7。
number03 = 0o10
# 十六进制HEX：每位用十六种状态计数，逢十六进一，写法是0~9  a(10)~f(15)。
number04 = 0x10

# 2. float 字面值
# 小数
number05 = 0.00001
# 科学计数法
number06 = 1e-5
print(number05)